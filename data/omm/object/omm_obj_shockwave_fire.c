static bool is_mario_in_range(struct Object *o, struct MarioState *m) {

    // Lateral distance
    f32 marioRadius = (m->action == ACT_OMM_POSSESSION ? omm_capture_get_wall_hitbox_radius(gOmmCapture) : (50.f * m->marioObj->oScaleX));
    f32 waveRadius = (o->oShockwaveFireRadius * o->oScaleX);
    f32 waveWidth = (o->oShockwaveFireWidth / 2.5f) + marioRadius;
    f32 distMin = waveRadius - waveWidth;
    f32 distMax = waveRadius + waveWidth;
    f32 distToMario = sqrtf(sqr_f(m->pos[0] - o->oPosX) + sqr_f(m->pos[2] - o->oPosZ));
    if (distMin > distToMario ||
        distMax < distToMario) {
        return false;
    }

    // Height check
    f32 yMin = o->oPosY;
    f32 yMax = o->oPosY + o->oShockwaveFireHeight;
    f32 yMarioMin = m->pos[1];
    f32 yMarioMax = m->pos[1] + (m->marioObj->hitboxHeight * m->marioObj->oScaleY);
    f32 sumOfHeights = o->oShockwaveFireHeight + m->marioObj->hitboxHeight;
    if ((yMarioMax - yMin) > sumOfHeights ||
        (yMax - yMarioMin) > sumOfHeights) {
        return false;
    }

    // Mario's butt is about to burn
    return true;
}

static void omm_bhv_shockwave_fire_update() {
    struct Object *o = gCurrentObject;
    if (o->oTimer > o->oShockwaveFireDuration) {
        obj_mark_for_deletion(o);
        return;
    }

    // Update scale
    f32 scale = (o->oShockwaveFireSpeed / o->oShockwaveFireRadius) * o->oTimer;
    o->oScaleX = scale;
    o->oScaleY = 1.f;
    o->oScaleZ = scale;

    // Update gfx
    OmmShockwaveFireGeoData *data = o->oGeoData;
    Vtx *vwave = data->vtx;
    Vtx *vfire = data->vtx + 130;
    f32 radius = o->oShockwaveFireRadius;
    f32 width  = o->oShockwaveFireWidth  * 1.60f / max_f(o->oScaleX, 0.01f);
    f32 height = o->oShockwaveFireHeight * 1.25f;
    s32 frame  = o->oTimer;
    u8  alpha  = 255.9f * clamp_0_1_f((o->oShockwaveFireDuration - o->oTimer) / 15.f);

    // Compute wave vtx
    for (s32 i = 0; i <= 64; i++, vwave += 2) {
        s16 angle = (s16) (i * 0x400);
        vwave[0].v.ob[0] = max_f(0, (radius - width / 2)) * sins(angle);
        vwave[0].v.ob[1] = 0;
        vwave[0].v.ob[2] = max_f(0, (radius - width / 2)) * coss(angle);
        vwave[0].v.tc[0] = (1 << 5) * 256 * (i % 2);
        vwave[0].v.tc[1] = (1 << 5) * 64;
        vwave[0].v.cn[0] = 0xFF;
        vwave[0].v.cn[1] = 0xFF;
        vwave[0].v.cn[2] = 0xFF;
        vwave[0].v.cn[3] = alpha;
        vwave[1].v.ob[0] = max_f(0, (radius + width / 2)) * sins(angle);
        vwave[1].v.ob[1] = 0;
        vwave[1].v.ob[2] = max_f(0, (radius + width / 2)) * coss(angle);
        vwave[1].v.tc[0] = (1 << 5) * 256 * (i % 2);
        vwave[1].v.tc[1] = (1 << 5) * 192;
        vwave[1].v.cn[0] = 0xFF;
        vwave[1].v.cn[1] = 0xFF;
        vwave[1].v.cn[2] = 0xFF;
        vwave[1].v.cn[3] = alpha;
    }

    // Compute fire vtx
    frame = (frame % 20);
    s16 u = (frame % 4) * 240;
    s16 v = (frame / 4) * 160;
    s32 m = (s32) ((2.f * M_PI * radius * o->oScaleX) / 200.f);
    s32 n = ((clamp_s(m, 8, 256) / 2) * 2); // Must be even
    for (s32 i = 0; i <= n; i++, vfire += 2) {
        s16 angle = (s16) ((i * 0x10000) / n);
        s32 du = 0;
        switch (i % 4) {
        case 0: du =   0; break;
        case 1: du = 120; break;
        case 2: du = 239; break;
        case 3: du = 120; break;
        }
        vfire[0].v.ob[0] = radius * sins(angle);
        vfire[0].v.ob[1] = 0;
        vfire[0].v.ob[2] = radius * coss(angle);
        vfire[0].v.tc[0] = (1 << 5) * (u + du);
        vfire[0].v.tc[1] = (1 << 5) * (v + 160);
        vfire[0].v.cn[0] = 0xFF;
        vfire[0].v.cn[1] = 0xFF;
        vfire[0].v.cn[2] = 0xFF;
        vfire[0].v.cn[3] = alpha;
        vfire[1].v.ob[0] = radius * sins(angle);
        vfire[1].v.ob[1] = height;
        vfire[1].v.ob[2] = radius * coss(angle);
        vfire[1].v.tc[0] = (1 << 5) * (u + du);
        vfire[1].v.tc[1] = (1 << 5) * (v + 0);
        vfire[1].v.cn[0] = 0xFF;
        vfire[1].v.cn[1] = 0xFF;
        vfire[1].v.cn[2] = 0xFF;
        vfire[1].v.cn[3] = alpha;
    }

    // Unused vtx
    for (s32 i = n + 1; i <= 256; ++i, vfire += 2) {
        OMM_MEMMOV(vfire, vfire - 1, sizeof(Vtx));
    }

    // Hitbox
    if (is_mario_in_range(o, gMarioState)) {
        f32 width = o->oShockwaveFireWidth / max_f(o->oScaleX, 0.01f);
        obj_set_params(o, INTERACT_FLAME, 1, 0, 0, true);
        obj_reset_hitbox(o, o->oShockwaveFireRadius + width, o->oShockwaveFireHeight, 0, 0, 0, 0);
    } else {
        obj_set_params(o, 0, 0, 0, 0, false);
        obj_reset_hitbox(o, 0, 0, 0, 0, 0, 0);
    }
}

